//! Events: the [`Event`] trait (with its [`Identifier`]/[`Specifier`]/[`Tags`]
//! components) and the [`Events`] buffer.
//!
//! An event is a plain struct carrying `#[revisioned(revision = N)]` (for
//! payload (de)serialisation) and `#[derive(Event)]` with an `#[event(..)]`
//! attribute that declares its `identifier` and its `tags`:
//!
//! ```
//! use eventric_domain::event::{
//!     Event,
//!     Identifier as _,
//! };
//! use revision::revisioned;
//!
//! #[revisioned(revision = 1)]
//! #[derive(Event)]
//! #[event(
//!     identifier: account_opened,
//!     tags: { account: id }
//! )]
//! struct AccountOpened {
//!     id: String,
//! }
//!
//! // `identifier()` returns the name as written; the stream indexes the event
//! // under its hash (`Identifier::type_name`).
//! assert_eq!(AccountOpened::identifier(), "account_opened");
//! ```
//!
//! See the [`Event`](macro@crate::event::Event) derive for the full
//! `#[event(..)]` grammar and the tag-value forms (bare ident, `&this.expr`,
//! closure, and `[list]` values for several tags under one prefix).

use std::collections::BTreeSet;

use error_stack::{
    Report,
    ResultExt as _,
};
pub use eventric_macros::Event;
use eventric_stream::{
    event::{
        Data,
        Event as StreamEvent,
        Facets,
        Name,
        Tag,
        Type,
        Version,
    },
    stream::operate::select::TypeSelector,
};
use fancy_constructor::new;
use revision::{
    DeserializeRevisioned,
    SerializeRevisioned,
};

use crate::error::Error;

