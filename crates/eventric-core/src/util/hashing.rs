use rapidhash::v3::{
    self,
    RapidSecrets,
};

// =================================================================================================
// Hashing
// =================================================================================================

// Configuration

static SEED: RapidSecrets = RapidSecrets::seed(0x2811_2017);

// -------------------------------------------------------------------------------------------------

// Hash

pub fn hash<T>(target: &T) -> u64
where
    T: AsRef<[u8]>,
{
    v3::rapidhash_v3_seeded(target.as_ref(), &SEED)
}
